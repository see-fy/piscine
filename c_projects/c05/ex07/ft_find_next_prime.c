#include <unistd.h>

int	ft_is_prime(int nb)
{
	long int	n;
	int			i;

	n = (long int)nb;
	i = 2;
	if (n <= 1)
		return (0);
	while (i <= n / 2)
	{
		if (n % i == 0)
			return (0);
        i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (!ft_is_prime(nb))
		nb++;
	return (nb);
}
